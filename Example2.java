class Example2 {
    public static void main(String[] args) {
        int num; // Об'ява змінної по ім'ю num
        num = 100;
        System.out.println("Значення num: " + num);
        num = num * 2;
        System.out.print("Значення num * 2: ");
        System.out.println(num);
    }
}

